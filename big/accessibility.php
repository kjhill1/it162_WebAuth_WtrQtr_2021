    <h2 class="<?$PageID?>">Usable Accessibility in Web Design</h2>
 
              <p>The most basic UX goal is to provide a visitor, with any level of cognitive and physical abilities, with a comfortable and engaging experience. If the site does not meet any user’s needs, it is a failure on the part of the Design Team. However, in web design, usability and accessibility are not the same thing but are directly related. Usability is the ease and learnability of using a website to access resources. While accessibility is the ability to utilize the assistive technology to allow equal access to resources as user.<br>

              <p>However, it is not just a question of equal access to resources for individuals with additional needs but also of equity of accessing said resources. The option of equal usage is not the same of the functionality of use of use For example, having the use to a screen reader on a medical or financial services website is useless if the features do not read clearly or slowly enough for the user to complete a transaction or schedule an appointment.<br>

          <h3><b>Best Practices for Webpage Design</b></h3>

              <p>Disabilities that require assistive technology are typically related to permanent states such as auditory, visual, limited motor and mobility. However, there also circumstantial states such as a broken arm or eye surgery. All of which require assistance to successfully navigate a website. <br>

              <p>To be sure the designer is considering all audiences, ensure all content meets the P.O.U.R* criteria:<br>
                <ul>
                    <li>Is the content <b>P</b>erceivable?</li>
                    <li>Are the interface elements<b>O</b>perable?</li>
                    <li>Are the content and controls <b>U</b>nderstandable? 
                    <li>Does the site offer <b>R</b>obust content to work with current and future technologies?</li>

                </ul>

            <p>There are many resources available that offer quick references of good design practice. The following are some of the most basic practices taken for the A11y Project’s website.<br>
            
              <ul>
                    <li>Validate all HTML and CSS></li>
                    <li>Create linear content flow with appropriate spacing in a logical sequence.</li>
                    <li>Eliminate horizontal scrolling.</li>
                    <li>Screen readers need to be compatible with all browsers and properly announce calls to action.</li>
                    <li>Always make sure HTML"img tag" include an alternative description “alt”.</li>
                    <li>Do not use autoplay  and ensure all media can easily be paused.</li>
                    <li>Use appropriately sized and high contrast colors for borders, icons, and text at any size.</li>
                    <li>Make sure that error, warning, and success states are not visually communicated only by color.</li>
                    <li>Avoid flashing or strobe light features which might trigger seizures</li>
                    <li>Ensure that screens rotate to any orientation.</li>
                    <li>Ensure that the tab button and arrow keys are functioning to allow for keyboard navigation only.</li>
                </ul>


          <h3><b>Keeping Up with the User’s Needs</b></h3>

              <p>Test, test, and more testing. User testing is the best way to ensure your site is accessible and usable for all. Don’t assume the needs of a person with disabilities. Do routine user testing with the appropriate audience. Since technology changes literally minute by minute, do the testing regularly. There are not only advancements in technology but also related medical fields.<br>

              <p>Most of all keep websites simple and logical, which should apply to any website considering usability. If it is confusing to a standard user, the design flaws are going to be amplified for those who need accommodations. </p>


