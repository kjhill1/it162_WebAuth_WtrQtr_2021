<h2 class="<?$PageID?>">Responsive and Adaptive Web Design</h2>
 
      <p>Over the past thirty years, computer-human interaction and user experience  thas dramatically changed with screen sizes and resolutions being at the top of the list. Specifically, a designer must consider how content is rendered on various size screens with any number of resolutions. Therefore, it is my job to provide the optimal user experience, which enables a client to remain a leader in their respective market.<br>

      <p>This does not mean just having content displayed correctly on a screen but is displayed effectively. This means users are engaged, entertained, and motivated enough to continue to use the site and return. As of this publishing, 53.1% of users are now on mobile devices to use the internet.*<br>
      <p>So, this warrants the question: Do we design for mobile or desktop first? Let us look at both methods.<br>

  <h3><b>What is Responsive Web Design?</b></h3>

      <p>RWD  is a method of building web pages with a layout to adjust in “response” to a device’s screen size, platform, and resolution. For instance, a desktop webpage layout is not appropriate for a cell phone. To ensure the user has the optimal experience, we use the CSS rule of “@media query” to detect the display’s size and how the browser will display the content. <br>
        
      <p>Therefore, some of the best practices for RWD are to create fluid grids and images, using Scalable Vector Graphics (SVGs), including three or more breakpoints (3 different kinds of devices,) then use the highest impact content for smaller devices and, use the compatible styles, fonts, and designs.<br>


<p>Some of the distinctive benefits of RWD: <br>

    <ul> 
        <li>Less scrolling because more content is visible in the screen.</li>    
        <li>Inexpensive due to the fact there is only one URL that needs to be owned.</li>
        <li>It is a single coded website, so it is easier for the designer to manage.</li>
        <li>Consistent design and branding.</li>
        <li>Easy to test, collect, and analyze data.</li>
    <ul>

  <h3><b>What is Adaptive Web Design (AWD)?</b></h3>

      <p>The alternative method of design for a successful user experience is AWD. AWD and RWD have the same goal: allow the user to visit any website, on any size viewport (i.e., cell phone, tablet, desktop) to successfully navigate the pages while representing the client.<br>

      <p>However, their building processes are completely different. Unlike RWD, AWD is static not fluid, meaning that the pages are not a singular website that adjust to fit the screen. The content is individually laid out for the viewport size and is assigned a dedicated URL. When the request for the site is made by the browser, it indicates the size of the page and which content should display from which website. It should be noted there are not multiple URLs that need to be used, they are all connected by the main webs address.</p>

    <p>These are the major advantages of AWD:<br>

    <ul>
          <li>Higher level of control over content design</li>
          <li>Faster download times because the browser is not adjusting the content but gets it directly from the source</li>
          <li>Allows forms, tables, and like content to be rendered consistently</li>
          <li>Swift loading time which is best suited for e-commerce and retail businesses, such as Amazon.com </li>
          <li>Provides a better experience for highly interactive sites</li>
          <li>Images are optimized based on the resolution and the size of the user’s device</li>
    </ul>

<h3><b>>Which is the better?</b></h3>

    <p>At this point in Web Design, there is no perfect answer. The designer should choose the best option based on the given job. Through experience and knowledge, we can utilize the best method to meet the client and user’s needs.<br>

